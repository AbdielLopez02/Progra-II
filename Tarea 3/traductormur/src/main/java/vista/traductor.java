private int contarCaracteres(String texto, char... caracteres) {
    int contador = 0;
    texto = texto.toLowerCase(); // Convertimos el texto a minúsculas para facilitar la comparación

    for (char c : texto.toCharArray()) {  // Iteramos sobre cada carácter del texto
        for (char caracter : caracteres) {
            if (c == caracter) {
                contador++;
            }
        }
    }

    return contador;
}


private void procesarTexto(String texto) {
    // Longitud del texto
    longitud_txt.setText(String.valueOf(texto.length()));

    // Palabras en total
    String[] palabras = texto.split("\\s+");
    ptotal_txt.setText(String.valueOf(palabras.length));

    // Primera letra del texto
    if (texto.length() > 0) {
        primer_txt.setText(String.valueOf(texto.charAt(0)));
    } else {
        primer_txt.setText("");
    }

    // Última letra del texto
    if (texto.length() > 0) {
        ulti_txt.setText(String.valueOf(texto.charAt(texto.length() - 1)));
    } else {
        ulti_txt.setText("");
    }

    // Letra del centro
    if (texto.length() > 0) {
        centro_txt.setText(String.valueOf(texto.charAt(texto.length() / 2)));
    } else {
        centro_txt.setText("");
    }

    // Primera palabra
    if (palabras.length > 0) {
        primerapalabra_txt.setText(palabras[0]);
    } else {
        primerapalabra_txt.setText("");
    }

    // Palabra central
    if (palabras.length > 0) {
        centralapalabra_txt.setText(palabras[palabras.length / 2]);
    } else {
        centralapalabra_txt.setText("");
    }

    // Última palabra
    if (palabras.length > 0) {
        ulimapalabra_txt.setText(palabras[palabras.length - 1]);
    } else {
        ulimapalabra_txt.setText("");
    }

    // Contar repeticiones de vocales en el texto completo
    reppA_txt.setText(String.valueOf(contarCaracteres(texto, 'a', 'á')));
    reppE_txt.setText(String.valueOf(contarCaracteres(texto, 'e', 'é')));
    reppI_txt.setText(String.valueOf(contarCaracteres(texto, 'i', 'í')));
    reppO_txt.setText(String.valueOf(contarCaracteres(texto, 'o', 'ó')));
    reppU_txt.setText(String.valueOf(contarCaracteres(texto, 'u', 'ú', 'ü')));



    // Palabras con cantidad de caracteres par e impar
    int pares = 0, impares = 0;
    for (String palabra : palabras) {
        if (palabra.length() % 2 == 0) {
            pares++;
        } else {
            impares++;
        }
    }
    par_txt.setText(String.valueOf(pares));
    impar_txt.setText(String.valueOf(impares));
}











// Método para traducir una cadena a la clave Murciélago
private String traducirClaveMurcielago(String texto) {
    // Definir la clave Murciélago
    String claveOriginal = "murcielago";
    String claveNueva = "0123456789";

    // Convertir el texto
    StringBuilder textoTraducido = new StringBuilder();

    for (char c : texto.toLowerCase().toCharArray()) {
        int indice = claveOriginal.indexOf(c);
        if (indice != -1) {
            // Reemplazar por el número correspondiente en la clave Murciélago
            textoTraducido.append(claveNueva.charAt(indice));
        } else {
            // Si el carácter no está en la clave, se mantiene igual
            textoTraducido.append(c);
        }
    }

    return textoTraducido.toString();
}
