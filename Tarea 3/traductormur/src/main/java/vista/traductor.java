    private void abrirArchivo() {
    JFileChooser fileChooser = new JFileChooser();
    int seleccion = fileChooser.showOpenDialog(this);

    if (seleccion == JFileChooser.APPROVE_OPTION) {
        archivoActual = fileChooser.getSelectedFile();
        archivoGuardado = true; 
        try {
            // Leer el archivo
            BufferedReader reader = new BufferedReader(new FileReader(archivoActual));
            StringBuilder texto = new StringBuilder();
            String linea;
            while ((linea = reader.readLine()) != null) {
                texto.append(linea).append("\n");
            }
            reader.close();

            // Mostrar el contenido en el área de texto original
            pantallaentrada_txt.setText(texto.toString());

            // Traducir el texto y mostrarlo en el área correspondiente
            String textoTraducido = traducirClaveMurcielago(texto.toString());
            taduc_txt.setText(textoTraducido);

            // Procesar estadísticas del texto original
            procesarTexto(texto.toString());

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error al abrir el archivo: " + ex.getMessage());
        }
    }
}
private void guardarArchivo(File archivo) {
   try (BufferedWriter writer = new BufferedWriter(new FileWriter(archivo))) {
        writer.write(pantallaentrada_txt.getText());  // Guardar el texto original
        JOptionPane.showMessageDialog(this, "Archivo guardado exitosamente.");
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Error al guardar el archivo: " + ex.getMessage());
    }
}

